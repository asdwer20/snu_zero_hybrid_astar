//compares state to map to find if there is an obstacle
template<typename T> bool valid_state_check(std::vector<std::vector<T>> map, std::vector<int> state) {
	if (state[0] >= map.size() || state[1] >= map[0].size()) {
		return false;
	} else if (state[0] < 0 || state[1] < 0) {
		return false;
	} else if (map[state[0]][state[1]] == 1) { // 100
		return false;
	} else {
		return true;
	}
}

template<typename T> void print_2Dvector(std::vector<std::vector<T>> input) {
	if (input.empty()) {
		std::cout <<"empty \n";
	} else for (int i = 0; i < input.size(); i++) {
		for (int j = 0; j < input[i].size(); j++) {
			std::cout << std::left << std::setw(3) << input[i][j] << " ";
		}
		std::cout << std::endl;
	}
}

template<typename T> void print_2Dvector_quote(std::vector<std::vector<T>> input) {
	if (input.empty()) {
		std::cout <<"empty \n";
	} else for (int i = 0; i < input.size(); i++) {
		for (int j = 0; j < input[i].size(); j++) {
			std::cout << "'" << input[i][j] << "' ";
		}
		std::cout << std::endl;
	}
}

template<typename T> void print_2Dvector_fancy(std::vector<std::vector<T>> input) {
	char fancy = '_';
	if (input.empty()) {
		std::cout <<"empty \n";
	} else for (int i = 0; i < input.size(); i++) {
		for (int j = 0; j < input[i].size(); j++) {
			if (input[i][j] == 0) fancy = '_';
			else if (input[i][j] == 1) fancy = 'X';
			else if (input[i][j] == 2) fancy = '?';
			else if (input[i][j] == 3) fancy = '!';
			else if (input[i][j] == 8) fancy = '@';
                        else fancy = '$';
			std::cout << fancy << " ";
		}
		std::cout << std::endl;
	}
}

